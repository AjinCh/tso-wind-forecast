COMMENT ON TABLE forecasts IS 'Summary of wind forecasts generated by the system';
COMMENT ON TABLE predictions IS 'Detailed hourly predictions for each forecast';
COMMENT ON TABLE alerts IS 'Log of alerts sent for each forecast';

